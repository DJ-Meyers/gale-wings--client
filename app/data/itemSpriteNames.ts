/**
 * PokemonDB slug for an item not in the map above (a safety fallback; the
 * ItemIcon uses it against img.pokemondb.net when a local sprite is absent).
 */
export const toSlug = (s: string): string =>
  s
    .toLowerCase()
    .replaceAll(/['‘’]/g, "")
    .replaceAll(/\.\s*/g, "-")
    .replaceAll(/\s+/g, "-")
    .replaceAll(/-+/g, "-")
    .replaceAll(/^-|-$/g, "")
